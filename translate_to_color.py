def ttc(num):
    if num==0:
        return('纯白色')
    elif num==1:
        return('橙红色')
    elif num==2:
        return('天蓝色')
    elif num==3:
        return('青绿色')
    elif num==4:
        return('纯橙色')
    elif num==5:
        return('纯紫色')
    elif num==6:
        return('粉红色')
    else:
        return('空')